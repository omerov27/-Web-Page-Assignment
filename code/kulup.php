      <h3 style="margin-right:1210px">Kurum Yılı : 1907</h3>
      <h3 style="color:black; margin-top:-90px ; margin-right:725px;">Kurum Yeri : Moda Beşbıyık Sokakğı 3 numaralı evin altı</h3>
      <p>1895 yılında Moda’da oturan İngilizlerin modern futbolu oynamaya başlamaları, Fenerbahçe Spor Kulübü’nün kurulmasının ilk adımları olacaktı. Deniz öğrencisi Fuat Hüsnü Kayacan’ın, 1899 yılında Fenerbahçe Stadı’nın bulunduğu çayırda meşin yuvarlağa yaptığı vuruşlar sırasında arkadaşları Reşat Denyal, Mehmet Ali ile dile getirdikleri "Ah biz de bir futbol takımı kurup oynayabilsek" özlemi, Türk gençleri arasında Black Stockings FC kurulmasına sebep olmuştur. Fakat daha sonra, kulüp monarşi rejiminin engellenmesini önlemek amacıyla hemen dağıtılmıştır.</p>
      <p>Birkaç gencin de katılımıyla aynı isimler, 1902 senesinde bu kez Kadıköy Futbol Kulübü adı altında toplandılar. Ancak daha sert hafiye baskını bu girişimi de engellemiştir. 1907 yılının bir bahar gününde gene bir maç dönüşü Ziya, Ayetullah ve Necip evlerinde çay içerlerken sönmeyen ideallerini bir kez daha başarmaya yöneldiler. Monarşi rejimi artık gevşemiş ve bu girişim bu kez tutunmuş ve <strong style="color: blue;">FENERBAHÇE FUTBOL KULÜBÜ</strong> bir daha kapatılmamak üzere kurulmuştur.</p>
      <p>Fenerbahçe Futbol Kulübü’nün ilk yönetim kurulu şöyledir: Ziya Bey "Başkan", Ayetullah Bey "Genel Sekreter" ve Necip Bey de "Genel Kaptan ve Veznedar"dır. Tabii kuruluş yılları kolay olmamış, zaman zaman futbolcu bulmakta zorlanılmış ve birçok defa gemilerden ödünç futbolcu alarak ligdeki mücadelesini sürdürülmüştür. 1909 yılında kulübün adı Fenerbahçe Spor Kulübü olarak değişmiş, renkleri de sarı-beyazdan bugünkü rengi olan sarı-laciverde çevrilmiştir. 1909-1911 yılları Fenerbahçe’miz için çok zor geçmiş bir ara dağılma noktasına bile gelinmiş ancak Elkatipzade Mustafa adlı üye, kulübü kurtaran adam olmuştur.</p>
      <p>Lokali dahi olmayan kulübün takımları çok kötü durumdayken St. Joseph, Robert College ve Kadıköy Numune Mektebi’nden toplanılan genç futbolcularla, kulübün genç takımları kurulmuş, bir nevi alt yapısını oluşturulmuştur. Bu atılım, başarısız geçen 2 yılın ardından Fenerbahçe’ye hiç yenilmeden ilk şampiyonluğunu getirmiştir.</p>
      <p>Bu şampiyonluk ise, Fenerbahçe’ye yaşama gücü aşılamış ve kulüp Altıyol ağzında 2 odalı bir lokale kavuşmuştur. Balkan Savaşı nedeni ile yapılmayan 1912-1913 lig maçlarından sonra üst üste ve yenilmeden kazanılan 2 şampiyonluk, Fenerbahçe camiasını oluşturmaya başlamıştır. Fenerbahçemiz aynı zamanda 1914 senesinde tertiplenen Genç takımlar şampiyonluğunu da kazanmış ve 10 yıl içinde en çok şampiyonluk kazanmış takım olma unvanını alarak İngilizler tarafından verilen tarihsel şilti de almaya hak kazanmıştır.</p>
